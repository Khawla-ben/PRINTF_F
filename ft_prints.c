#include "ft_printf.h"

int	ft_prints(char *c)
{
	int	i ;

	i = 0;
	if (!c)
		return (ft_prints("(null)"));
	while (c[i])
	{
		ft_putchar(c[i]);
		i++;
	}
	return (i);
}
